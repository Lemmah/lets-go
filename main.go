package main

func main() {
	// Addition
	add := 2 + 2
	println(add)
	// Subtraction
	sub := add - 3 // Quick math
	println(sub)
	// Muliplication
	mult := add * add
	println(mult)
	// Modulous
	modulo := add % sub
	println(modulo)
	// Augmented Assignment Operators
	modulo += 2
	println(modulo)
	modulo *= 2
	println(modulo)
	modulo /= 2
	println(modulo)
}
